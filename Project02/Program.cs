using System;
namespace SeminarTwo
{
    class Program
    {
        static void Main(string[] args)
        {

                int n = 0;
                n = int.Parse(Console.ReadLine());

                for (int i = 1; i <= n; i++)
                {
                    int b = i * i;
                    Console.WriteLine(b + " ");

                }

        }

    }

         


